
package DTO;


public class TrabalhosDTO {
    private int id_trabalhos;
    private String contratante_trabalhos;
    private String endereco_trabalhos;
    private String descricao_trabalhos;
    private String telefone_trabalhos;

    public int getId_trabalhos() {
        return id_trabalhos;
    }

    public void setId_trabalhos(int id_trabalhos) {
        this.id_trabalhos = id_trabalhos;
    }

    public String getContratante_trabalhos() {
        return contratante_trabalhos;
    }

    public void setContratante_trabalhos(String contratante_trabalhos) {
        this.contratante_trabalhos = contratante_trabalhos;
    }

    public String getEndereco_trabalhos() {
        return endereco_trabalhos;
    }

    public void setEndereco_trabalhos(String endereco_trabalhos) {
        this.endereco_trabalhos = endereco_trabalhos;
    }

    public String getDescricao_trabalhos() {
        return descricao_trabalhos;
    }

    public void setDescricao_trabalhos(String descricao_trabalhos) {
        this.descricao_trabalhos = descricao_trabalhos;
    }

    public String getTelefone_trabalhos() {
        return telefone_trabalhos;
    }

    public void setTelefone_trabalhos(String telefone_trabalhos) {
        this.telefone_trabalhos = telefone_trabalhos;
    }
    
    
}
