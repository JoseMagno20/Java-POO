
package DTO;


public class MateriaisDTO {
    
    private int id_materiais;
    private String nome_materiais;
    private String descricao_materiais;

    public int getId_materiais() {
        return id_materiais;
    }

    public void setId_materiais(int id_materiais) {
        this.id_materiais = id_materiais;
    }

    public String getNome_materiais() {
        return nome_materiais;
    }

    public void setNome_materiais(String nome_materiais) {
        this.nome_materiais = nome_materiais;
    }

    public String getDescricao_materiais() {
        return descricao_materiais;
    }

    public void setDescricao_materiais(String descricao_materiais) {
        this.descricao_materiais = descricao_materiais;
    }
    
    
    
    
}
